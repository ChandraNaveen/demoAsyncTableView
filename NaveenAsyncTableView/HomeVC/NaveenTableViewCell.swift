import UIKit

class NaveenTableViewCell: UITableViewCell {
    
    
    
    let nameLabel = UILabel()
    let detailLabel = UILabel()
    let imageContainer = UIImageView()
    
    // MARK: Initalizers
    override init(style: UITableViewCell.CellStyle, reuseIdentifier: String?) {
        super.init(style: style, reuseIdentifier: reuseIdentifier)
        
        let marginGuide = contentView.layoutMarginsGuide
        
        
        // configure imageView
        contentView.addSubview(imageContainer)
        imageContainer.layer.cornerRadius =  8;
        imageContainer.leadingAnchor.constraint(equalTo: marginGuide.leadingAnchor).isActive = true
        imageContainer.topAnchor.constraint(equalTo: marginGuide.topAnchor).isActive = true
        imageContainer.trailingAnchor.constraint(equalTo: marginGuide.trailingAnchor).isActive = true
        imageContainer.layer.masksToBounds = true;
        imageContainer.contentMode = .scaleAspectFill
        imageContainer.translatesAutoresizingMaskIntoConstraints = false
        
        // configure titleLabel
        contentView.addSubview(nameLabel)
        nameLabel.translatesAutoresizingMaskIntoConstraints = false
        nameLabel.leadingAnchor.constraint(equalTo: imageContainer.trailingAnchor).isActive = true
        nameLabel.topAnchor.constraint(equalTo: marginGuide.topAnchor).isActive = true
        nameLabel.trailingAnchor.constraint(equalTo: marginGuide.trailingAnchor).isActive = true
        nameLabel.numberOfLines = 0
        nameLabel.font = UIFont(name: "AvenirNext-DemiBold", size: 16)
        
        // configure authorLabel
        contentView.addSubview(detailLabel)
        detailLabel.translatesAutoresizingMaskIntoConstraints = false
        detailLabel.leadingAnchor.constraint(equalTo: imageContainer.trailingAnchor).isActive = true
        detailLabel.bottomAnchor.constraint(equalTo: marginGuide.bottomAnchor).isActive = true
        detailLabel.trailingAnchor.constraint(equalTo: marginGuide.trailingAnchor).isActive = true
        detailLabel.topAnchor.constraint(equalTo: nameLabel.bottomAnchor).isActive = true
        detailLabel.numberOfLines = 0
        detailLabel.font = UIFont(name: "Avenir-Book", size: 12)
        detailLabel.textColor = UIColor.lightGray
        
    }
    
    required init?(coder aDecoder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    
    
}
